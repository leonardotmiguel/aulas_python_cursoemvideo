# Crie um script em Python que leia dois números e tente mostrar a soma entre eles.
# Aula 04, Mundo 01
# Usa-se int() para converter o que foi digitado (string) no input em um número inteiro, permitindo que possa fazer contas matemáticas com ele.

n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))
soma = n1 + n2

print(f'O resultado da soma entre o n1 e n2 é {soma}')