# Uma variável é como uma "caixa" que guarda um valor.
# Podemos dar um nome a essa caixa e guardar diferentes tipos de informação nela.
# Por exemplo, aqui estamos criando uma variável (caixa) chamada "nome" que guarda uma informação (Leonardo)

nome = 'Leonardo'
idade = 30
peso = 95

print(f'Meu nome é {nome}, minha idade é {idade} anos, e meu peso é {peso}kg.')